package com.Robel;

//Updates Location of Character in a Maze
public class Location {

    //X and Y coordinates
    int x,y;


    //No-arg Constructor
    Location(){
        x = 1;
        y = 1;
    }

    //Constructor
    Location(int x, int y){
        this.x = x;
        this.y = y;
    }
}
